    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        // TODO add your handling code here:
        if (txtIDProduk.getText().isBlank() || txtNamaProduk.getText().isBlank() || cbKategori.getSelectedItem() == null || txtHarga.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Harap mengisi data secara keseluruhan", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String idProduk = txtIDProduk.getText();
        String namaProduk = txtNamaProduk.getText();
        String hargaProduk = txtHarga.getText();
        String kategori = cbKategori.getSelectedItem().toString();
        
        try {           

        Connection cnVar = koneksi.getKoneksi();
        String sqlCheck = "SELECT * FROM makanminum WHERE id = ?";
        PreparedStatement psVarCheck =  cnVar.prepareStatement(sqlCheck);
        psVarCheck.setString(1, idProduk);
        ResultSet rsVarCheck = psVarCheck.executeQuery();
        
        if (rsVarCheck.next()) {
            JOptionPane.showMessageDialog(null, "Warning! Data yang diinputkan sudah ada, harap mengisi data dengan ID lain", "Warning", JOptionPane.WARNING_MESSAGE);
            reset();
            return;
            
        } else {
            
            String sql = "INSERT INTO makanminum VALUES (?, ?, ?, ?)";
            PreparedStatement psVar = cnVar.prepareStatement(sql);
            
            psVar.setString(1, idProduk);
            psVar.setString(2, namaProduk);
            psVar.setString(3, kategori);
            psVar.setString(4, hargaProduk);
            psVar.execute();
            
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            
            }  
        
        } catch (SQLException sQLException) {
            JOptionPane.showMessageDialog(null, "Error, Data gagal disimpan " + sQLException.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        reset();
        data_makan_minum();
    }//GEN-LAST:event_buttonSimpanActionPerformed

    private void buttonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditActionPerformed
        // TODO add your handling code here:
        if (txtIDProduk.getText().isBlank() || txtNamaProduk.getText().isBlank() || cbKategori.getSelectedItem() == null || txtHarga.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Harap memilih data yang ingin diubah, atau silahkan isi data secara keseluruhan", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String idProduk = txtIDProduk.getText();
        String namaProduk = txtNamaProduk.getText();
        String hargaProduk = txtHarga.getText();
        String kategori = cbKategori.getSelectedItem().toString();
        
        try {
            
            String sql = "UPDATE makanminum SET nama = ?, kategori = ?, harga = ? WHERE id = ?";
            Connection cnVar = koneksi.getKoneksi();
            PreparedStatement psVar = cnVar.prepareStatement(sql);
            
            psVar.setString(1, namaProduk);
            psVar.setString(2, kategori);
            psVar.setString(3, hargaProduk);
            psVar.setString(4, idProduk);
            psVar.execute();
            
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            
        } catch (SQLException sQLException) {
            JOptionPane.showMessageDialog(null, "Error, Data gagal diubah : " + sQLException.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        reset();
        data_makan_minum();
    }//GEN-LAST:event_buttonEditActionPerformed

