    void data_makan_minum(){
        
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("ID Produk");
        model.addColumn("Nama Produk");
        model.addColumn("Kategori Produk");
        model.addColumn("Harga Produk");
        
        String sql = "SELECT * FROM makanminum";
        try {
            
            Connection cnVar = koneksi.getKoneksi();
            Statement psVar = cnVar.createStatement();
            ResultSet rsVar = psVar.executeQuery(sql);
            
            while (rsVar.next()) {                
                String idProduk = rsVar.getString("id");
                String namaProduk = rsVar.getString("nama");
                String kategoriProduk = rsVar.getString("kategori");
                String hargaProduk = rsVar.getString("harga");
                
                Object[] data = {idProduk, namaProduk, kategoriProduk, hargaProduk};
                model.addRow(data);
            }
            
        } catch (SQLException sQLException) {
            JOptionPane.showMessageDialog(null, "Gagal mengambil data " + sQLException.getMessage());
        }
        tableMakanMinum.setModel(model);
    }

     void cari_data(){
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("ID Produk");
        model.addColumn("Nama Produk");
        model.addColumn("Kategori Produk");
        model.addColumn("Harga Produk");
        
        String cari = txtCari.getText();
        String sql = "SELECT * FROM makanminum WHERE nama LIKE ? OR kategori LIKE ? OR harga LIKE ?";
        
        try {
            Connection cnVar = koneksi.getKoneksi();
            PreparedStatement psVar = cnVar.prepareStatement(sql);
            psVar.setString(1, "%" + cari + "%");
            psVar.setString(2, "%" + cari + "%");
            psVar.setString(3, "%" + cari + "%");
            ResultSet rsVar = psVar.executeQuery();
            
            while (rsVar.next()) {                
                String idProduk = rsVar.getString("id");
                String namaProduk = rsVar.getString("nama");
                String kategoriProduk = rsVar.getString("kategori");
                int hargaProduk = rsVar.getInt("harga");
                
                Object[] data = {idProduk, namaProduk, kategoriProduk, hargaProduk};
                model.addRow(data);
            }
            tableMakanMinum.setModel(model);
            
        } catch (SQLException sQLException) {
            JOptionPane.showMessageDialog(null, "Error " + sQLException.getMessage());
        }
       
    }
