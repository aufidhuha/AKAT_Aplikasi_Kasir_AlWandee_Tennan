    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String userLama = txtUsernameLama.getText();
        String userBaru = txtUsernameBaru.getText();
        String passBaru = txtPasswordBaru.getText();
        String pengguna = txtNamaPengguna.getText();
        
        if (txtUsernameLama.getText().isBlank() || txtUsernameBaru.getText().isBlank() || txtPasswordBaru.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Harap mengisi data keseluruhan", "Peringatan", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        try {
            String sql = "UPDATE user SET username = ?, password = md5(?), nama = ? WHERE username = ?";
            Connection cnVar = koneksi.getKoneksi();
            
            PreparedStatement psVar = cnVar.prepareStatement(sql);
            psVar.setString(1, userBaru);
            psVar.setString(2, passBaru);
            psVar.setString(3, pengguna);
            psVar.setString(4, userLama);
            
            int konfirmasi = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin merubah\nUsername dan Password?", "Konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            
            if (konfirmasi == JOptionPane.YES_OPTION) {
                int psVarUpdate = psVar.executeUpdate();
                
                if (psVarUpdate > 0) {                    
                    reset();
                        JOptionPane.showMessageDialog(null, "Username dan Password berhasil diubah");
                        this.getTopLevelAncestor().setVisible(false);
                        new loginFrame().setVisible(true);                    
                } else {
                    JOptionPane.showMessageDialog(null, "Username lama salah, Username dan Password gagal diubah");
                }
            }
            
            psVar.close();
            cnVar.close();
        } catch (SQLException sQLException) {
                JOptionPane.showMessageDialog(null, "Error : " + sQLException.getMessage());
        }
        reset();
    }//GEN-LAST:event_jButton1ActionPerformed

